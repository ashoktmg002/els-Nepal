let currentLang = 'ne';

function setLanguage(lang) {
  // Standard data-key elements
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });
  // Team names/roles using data-key-ne / data-key-en
  document.querySelectorAll('[data-key-ne]').forEach(el => {
    const keyNe = el.getAttribute('data-key-ne');
    const keyEn = el.getAttribute('data-key-en');
    const key = lang === 'ne' ? keyNe : keyEn;
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });
  document.documentElement.lang = lang === 'ne' ? 'ne' : 'en';
  document.body.style.fontFamily = lang === 'ne'
    ? "'Noto Sans Devanagari', 'Outfit', sans-serif"
    : "'Outfit', 'Noto Sans Devanagari', sans-serif";
    // Update page title
  document.title = lang === 'ne'
    ? 'वातावरण कानून समाज नेपाल'
    : 'Environment Law Society Nepal';
}

setLanguage('ne');

function toggleLang() {
  if (currentLang === 'en') {
    currentLang = 'ne';
    setLanguage('ne');
    document.getElementById('langLabel').textContent = 'ENG';
  } else {
    currentLang = 'en';
    setLanguage('en');
    document.getElementById('langLabel').textContent = 'NEP';
  }
}

// HERO SLIDER
let currentSlide = 0;
const slides = document.querySelectorAll('.slide');
const dots = document.querySelectorAll('.hero-dot');
const slideNumEl = document.getElementById('slideNum');
let slideTimer;

function goToSlide(n) {
  slides[currentSlide].classList.remove('active');
  dots[currentSlide].classList.remove('active');
  currentSlide = (n + slides.length) % slides.length;
  slides[currentSlide].classList.add('active');
  dots[currentSlide].classList.add('active');
  slideNumEl.textContent = String(currentSlide + 1).padStart(2, '0');
  resetTimer();
}
function nextSlide() { goToSlide(currentSlide + 1); }
function prevSlide() { goToSlide(currentSlide - 1); }
function resetTimer() { clearInterval(slideTimer); slideTimer = setInterval(nextSlide, 6000); }
resetTimer();

window.addEventListener('load', () => {
  slides[0].classList.add('active');
  dots[0].classList.add('active');
});

